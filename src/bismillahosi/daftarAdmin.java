    private void tableAdminMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableAdminMouseClicked
        try{
            int row = tableAdmin.rowAtPoint(evt.getPoint());
            
            String id_admin = tableAdmin.getValueAt(row, 0).toString();
            String password = tableAdmin.getValueAt(row, 1).toString();
            String nama = tableAdmin.getValueAt(row, 2).toString();
            String tempatlahir = tableAdmin.getValueAt(row, 3).toString();
            String tanggallahir = tableAdmin.getValueAt(row, 4).toString();
            String gender = tableAdmin.getValueAt(row, 5).toString();
            
            
            String no_telp = tableAdmin.getValueAt(row, 6).toString();
            
            
            txt_id.setText(String.valueOf(id_admin));
            txt_pass.setText(String.valueOf(password));
            txt_nama.setText(String.valueOf(nama));
            txt_tempat.setText(String.valueOf(tempatlahir));
            dateTgl.setText(String.valueOf(tanggallahir));
            boxGender.setSelectedItem(String.valueOf(gender));
            txt_telp.setText(String.valueOf(no_telp));
            
        }
        catch(Exception e){
            
        }
    }//GEN-LAST:event_tableAdminMouseClicked

