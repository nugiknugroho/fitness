    public void tampil_database(){
        Object [] baris = {"ID","Password","Nama","Tempat","Tanggal","Gender","No_telp"};
        tabmode = new DefaultTableModel(null, baris);
        tableAdmin.setModel(tabmode);
        
        try{
            hasil = model.tampilData();
            while(hasil.next()){
                model.setID(hasil.getString("id_admin"));
                model.setPassword(hasil.getString("password"));
                model.setNama(hasil.getString("nama"));
                model.setTempat(hasil.getString("tempatlahir"));
                model.setTanggal(hasil.getString("tanggallahir"));
                model.setGender(hasil.getString("gender"));
                model.setTelp(hasil.getString("no_telp"));
                String [] data = {model.getID(), model.getPassword(), model.getNama(), model.getTempat(), model.getTanggal(),
                    model.getGender(), model.getTelp()};
                tabmode.addRow(data);
            }
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }
    
